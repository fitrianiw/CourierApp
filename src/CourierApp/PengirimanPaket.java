package CourierApp;

public class PengirimanPaket {
    private String NomorPaket;
    private String KotaTujuanPaket;
    private String JenisBarangPaket;
    private String BeratBarangPaket;
    private String TanggalKirim;
    private String PanjangDimensi;
    private String LebarDimensi;
    private String TinggiDimensi;
    private String JenisLayananPaket;
    private String PilihanAsurnsiPaket;
    private String HargaBarangPaket;
    private String PembayaranPaket;

    public PengirimanPaket() {
    }

    public PengirimanPaket(String NomorPaket, String KotaTujuanPaket, String JenisBarangPaket, String BeratBarangPaket, String TanggalKirim, String PanjangDimensi, String LebarDimensi, String TinggiDimensi, String JenisLayananPaket, String PilihanAsurnsiPaket, String HargaBarangPaket, String PembayaranPaket) {
        this.NomorPaket = NomorPaket;
        this.KotaTujuanPaket = KotaTujuanPaket;
        this.JenisBarangPaket = JenisBarangPaket;
        this.BeratBarangPaket = BeratBarangPaket;
        this.TanggalKirim = TanggalKirim;
        this.PanjangDimensi = PanjangDimensi;
        this.LebarDimensi = LebarDimensi;
        this.TinggiDimensi = TinggiDimensi;
        this.JenisLayananPaket = JenisLayananPaket;
        this.PilihanAsurnsiPaket = PilihanAsurnsiPaket;
        this.HargaBarangPaket = HargaBarangPaket;
        this.PembayaranPaket = PembayaranPaket;
    }
    
    
}
